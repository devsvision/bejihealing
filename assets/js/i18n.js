export function getLanguage() {
  return localStorage.getItem(STORAGE_KEY) || "id";
}

export function setLanguage(language) {
  localStorage.setItem(STORAGE_KEY, language === "en" ? "en" : "id");
  window.dispatchEvent(new CustomEvent("language:change", { detail: { language: getLanguage() } }));
}

export function t(key) {
  const language = getLanguage();
  return dictionaries[language]?.[key] || dictionaries.id[key] || key;
}

export function statusLabel(value) {
  const map = {
    paid: "paidStatus",
    confirmed: "confirmedStatus",
    pending: "pendingStatus",
    completed: "completedStatus",
    cancelled: "cancelledStatus",
    waiting: "waitingStatus",
    "in-progress": "inProgressStatus",
    unpaid: "unpaidStatus",
    deposit: "depositStatus",
    normal: "normalPriority",
    high: "highPriority",
    vip: "vipPriority",
    active: "active",
    inactive: "inactive"
  };
  return t(map[value] || value);
}

export function applyI18n(scope = document) {
  scope.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  scope.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.setAttribute("placeholder", t(node.dataset.i18nPlaceholder));
  });
  scope.querySelectorAll("[data-i18n-aria]").forEach((node) => {
    node.setAttribute("aria-label", t(node.dataset.i18nAria));
  });
  scope.querySelectorAll("[data-i18n-title]").forEach((node) => {
    node.setAttribute("title", t(node.dataset.i18nTitle));
  });
  scope.querySelectorAll("[data-language-select]").forEach((node) => {
    node.value = getLanguage();
  });
}
